//Arrow functions
const raizes = (numero) => Math.sqrt(numero)
console.log(raizes(9))
console.log(raizes(16))
console.log(raizes(25))

const messager = (name) => `hellou, ${name}`;
console.log(messager("Marcos"))

const contaMais = (numero1, numero2) => numero1 + numero2;
console.log(contaMais(15,2))