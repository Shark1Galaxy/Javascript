// Calcular a Área de um retângulo
//Um retângulo é um quadrilátero com quatro ângulos retos (90°) e lados opostos iguais.
const baseRetangulo = 8;
const alturaRetangulo = 5;
const areaRetangulo  = baseRetangulo * alturaRetangulo ;
console.log(areaRetangulo  + 'cm²')

//Perímetro de um retângulo
//O perímetro é a soma de todos os lados do retângulo, ou seja, o contorno do retângulo.
const perimetroRetangulo  = 2 * (baseRetangulo + alturaRetangulo)
console.log(perimetroRetangulo  + 'cm')
